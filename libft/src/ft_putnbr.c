#include "../inc/libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n / 10 > 0)
	{
		ft_putnbr(n / 10);
		ft_putchar((char)(n % 10) + 48);
	}
	else
		ft_putchar((char)n + 48);
}
