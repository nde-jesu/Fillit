#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i] && s1[i] == s2[i] && i < n && s2[i])
		++i;
	if (i == n || (!s2[i] && !s1[i]))
		return (1);
	return (0);
}
