int		ft_atoi(const char *str)
{
	int		i;
	int		ret;
	int		fl;

	ret = 0;
	i = 0;
	fl = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		++i;
	if (str[i] == '-')
		fl = -1;
	if (str[i] == '-' || str[i] == '+')
		++i;
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret *= 10;
		ret += str[i] - '0';
		++i;
	}
	return (ret * fl);
}
